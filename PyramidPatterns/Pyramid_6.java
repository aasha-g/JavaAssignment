public class Pyramid_6 {

	public static void main(String[] args) {
		 int r,x;
			
			for(r=1;r<=5;r++)                 //r=1
			{
			   for(x=1;x<=r;x++)              //x=1
			{
				System.out.print(x+" ");      //1 
				                             // 1 2
			}	
				System.out.println();
			}

	}

}

